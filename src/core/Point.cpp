#include "Point.h"

////////////////////////////////////////////////////////////////////////////////////////
//                          Constructeurs et Destructeur                              //
////////////////////////////////////////////////////////////////////////////////////////

Point::Point() {
    //Si le point est crée sans données de base, alors l'abcisse et l'ordonnée sont à 0
    x = 0;
    y = 0;
}

Point::Point(uint nx, uint ny) {
    x = nx;
    y = ny;
}

////////////////////////////////////////////////////////////////////////////////////////
//                                      Accesseurs                                    //
////////////////////////////////////////////////////////////////////////////////////////

int Point::getX() const {
    return x;
}

int Point::getY() const {
    return y;
}

////////////////////////////////////////////////////////////////////////////////////////
//                                      Mutateurs                                     //
////////////////////////////////////////////////////////////////////////////////////////

void Point::setX(const int nx) {
    x = nx;
}

void Point::setY(const int ny) {
    y = ny;
}